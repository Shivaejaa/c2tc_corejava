package com.cg.overloading;

public class Overloading {
	public static int addition(int a, int b) {
		return a+b;
	}
	
	public static int addition(int a, int b, int c) {
		return a+b;
	}
	
	public static float addition(float a, float b) {
		return a+b;
	}
}

